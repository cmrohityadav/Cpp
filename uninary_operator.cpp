#include<iostream>
using namespace std;

int main()
{
    // ++a:increment first then used
    // a++:use first then increment

    int a=10;
    cout<<a++<<endl;
    cout<<a<<endl;
    cout<<++a<<endl;
cout<<"decrement"<<endl;
    cout<<a--<<endl;
    cout<<a<<endl;
    cout<<--a<<endl;
    return 0;
}
